import java.util.*;
public class LoopsTable {
    public static void main(String[] args) {
        int num1, num2;
        for (num2 = 1; num2 <= 3; num2++){
              for (num1 = 1; num1 <= 10; num1++)
              System.out.print(num2 + " * " + num1 + " = " + (num1 * num2) + "\n");
        System.out.println(" ");
        }
     }
        
}

